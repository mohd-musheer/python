# Integer data type
Integer_variable =  53
print('Integer:', Integer_variable)

# Float data type
Float_variable = 8.9
print('Float:', Float_variable)

# String data type
String_variable = 'Hello, madam ji'
print('String:', String_variable)

# Boolean data type
Boolean_variable = False 
print('Boolean:', Boolean_variable)

# List data type (mutable and ordered)
List_variable = [4,5,6,7,3]
print('List:', List_variable)

# Tuple data type (immutable and ordered)
tuple_variable = (2,5,6)
print('Tuple:', tuple_variable)

